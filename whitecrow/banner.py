SMOKE_CHARS = ["▓", "▒", "░", " "]


def add_smoke(lines, offset):
    result = list(lines)
    for i in range(3):
        row = 8 + i
        col = 75 + i + offset
        if row < len(result) and col < len(result[row]):
            l = list(result[row])
            l[col] = SMOKE_CHARS[(offset + i) % 4]
            result[row] = "".join(l)
    return result


def animate_banner(duration=2.0):
    cols, _ = shutil.get_terminal_size()

    with Live(console=console, refresh_per_second=10, transient=True, screen=False) as live:
        for step in range(20):
            lines = add_smoke(CROW, step % 4)
            art = "\n".join(lines)
            panel = Panel(
                Center(Text(art, style="bright_cyan")),
                width=min(90, cols - 4),
                border_style="bright_magenta",
                title=f"[bright_magenta]WhiteCrow v1.0.0[/]",
                subtitle="[dim]OSINT Investigation Tool[/]",
                padding=(0, 1),
            )
            live.update(panel)
            time.sleep(0.1)

    show_banner()


